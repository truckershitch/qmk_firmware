bool encoder_update_user(uint8_t index, bool clockwise) {
    if (is_keyboard_left()) {
        if (clockwise) {
            tap_code(KC_7);
        } else {
            tap_code(KC_8);
        }
    }
    else {
        if (clockwise) {
            tap_code(KC_G);
        } else {
            tap_code(KC_H);
        }
    }

    return true;
}

